def demangle_dot(dot_content):
    """
    Demangle C++ mangled names in dot file safely.
    Strips parameter types to avoid dot syntax errors.
    e.g. _Z14generateReportPKc → generateReport
    """
    def replace_mangled(match):
        name = match.group(0).strip('"')
        result = subprocess.run(
            ['c++filt', name],
            capture_output=True, text=True
        )
        clean = result.stdout.strip()
        clean = clean.split('(')[0]   # remove parameter types
        clean = clean.split('::')[-1] # remove namespace prefix
        return f'"{clean}"'

    return re.sub(r'"?_Z\w+"?', replace_mangled, dot_content)


def run_llvm_callgraph(cpp_file_path, project_root, results_dir):
    """
    Run LLVM CallGraph tool on a C++ file.
    Returns: (png_path, error_message)
    """
    callgraph_script = os.path.join(project_root, "CallGraph", "run.sh")

    try:
        # Step 1: Run run.sh → generates graph.dot + graph.text
        result = subprocess.run(
            ["bash", callgraph_script, cpp_file_path],
            capture_output=True,
            text=True,
            cwd=project_root
        )

        if result.returncode != 0:
            return None, f"LLVM Error:\n{result.stderr}"

        # Check graph.dot exists
        dot_path = os.path.join(project_root, "graph.dot")
        if not os.path.exists(dot_path):
            return None, "graph.dot not generated by LLVM"

        # Step 2: Demangle names and render to PNG
        clean_dot_path = os.path.join(results_dir, "graph_clean.dot")
        with open(dot_path, 'r') as f:
            dot_content = f.read()

        clean_content = demangle_dot(dot_content)
        with open(clean_dot_path, 'w') as f:
            f.write(clean_content)

        png_path = os.path.join(results_dir, "callgraph.png")
        render_result = subprocess.run(
            ["dot", "-Tpng", clean_dot_path, "-o", png_path],
            capture_output=True,
            text=True
        )

        if render_result.returncode != 0:
            return None, f"Graphviz Error:\n{render_result.stderr}"

        return png_path, None

    except FileNotFoundError as e:
        return None, f"Tool not found: {e}\nMake sure LLVM and Graphviz are installed."
    except Exception as e:
        return None, f"Unexpected error: {e}"


def save_uploaded_file(uploaded_file, suffix='.cpp'):
    """Save Streamlit uploaded file to a temp location."""
    tmp = tempfile.NamedTemporaryFile(
        suffix=suffix,
        delete=False,
        dir='/tmp'
    )
    tmp.write(uploaded_file.read())
    tmp.close()
    return tmp.name


def render_zoomable_image(png_path):
    """Render a PNG image with mouse wheel zoom and drag-to-pan."""
    with open(png_path, "rb") as img_f:
        encoded = base64.b64encode(img_f.read()).decode()

    zoom_html = f"""
<div style="
    overflow: hidden;
    width: 100%;
    height: 600px;
    border: 1px solid #ddd;
    border-radius: 8px;
    background: #fafafa;
    position: relative;
    cursor: grab;
">
    <div id="zoom_container" style="
        transform-origin: top left;
        position: absolute;
        top: 0; left: 0;
    ">
        <img src="data:image/png;base64,{encoded}"
             id="callgraph_img"
             style="display: block; width: 100%;"
        />
    </div>
</div>
<p style="font-size:12px; color:gray; margin-top:4px;">
    🖱️ Scroll to zoom &nbsp;|&nbsp; Click + drag to pan &nbsp;|&nbsp; Double-click to reset
</p>
<script>
    const container = document.getElementById('zoom_container');
    const wrapper   = container.parentElement;

    let scale = 1, posX = 0, posY = 0;
    let dragging = false, startX, startY;

    wrapper.addEventListener('wheel', function(e) {{
        e.preventDefault();
        const delta = e.deltaY > 0 ? -0.1 : 0.1;
        scale = Math.min(Math.max(0.3, scale + delta), 5);
        container.style.transform = `translate(${{posX}}px, ${{posY}}px) scale(${{scale}})`;
    }}, {{ passive: false }});

    wrapper.addEventListener('mousedown', function(e) {{
        dragging = true;
        startX = e.clientX - posX;
        startY = e.clientY - posY;
        wrapper.style.cursor = 'grabbing';
    }});
    window.addEventListener('mousemove', function(e) {{
        if (!dragging) return;
        posX = e.clientX - startX;
        posY = e.clientY - startY;
        container.style.transform = `translate(${{posX}}px, ${{posY}}px) scale(${{scale}})`;
    }});
    window.addEventListener('mouseup', function() {{
        dragging = false;
        wrapper.style.cursor = 'grab';
    }});
    wrapper.addEventListener('dblclick', function() {{
        scale = 1; posX = 0; posY = 0;
        container.style.transform = `translate(0px, 0px) scale(1)`;
    }});
</script>
"""
    components.html(zoom_html, height=640)


