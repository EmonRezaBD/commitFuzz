def render_callgraph_tab(cpp_path, file_label, project_root, results_dir):
    """Main render function called from dashboard.py"""

    st.header("Call Graph Analysis")
    st.write("Generates a static call graph using LLVM/Clang static analysis.")

    # Show selected file
    if file_label:
        st.info(f"Selected file: `{file_label}`")
    else:
        st.warning("Please upload a C++ file or enable sample files.")

    # Session state init
    if 'callgraph_png' not in st.session_state:
        st.session_state.callgraph_png = None

    # Analyze button
    if st.button("Generate Call Graph", type="primary"):
        if cpp_path is None:
            st.error("No file selected.")
        else:
            with st.spinner("Running LLVM analysis... this may take a few seconds"):
                png_path, error = run_llvm_callgraph(
                    cpp_path, project_root, results_dir
                )
            if error:
                st.error(f"Error:\n\n{error}")
            elif png_path and os.path.exists(png_path):
                st.session_state.callgraph_png = png_path
                st.success("Call graph generated successfully!")
            else:
                st.error("Call graph image not found after analysis.")

    # Display graph (persists via session state)
    if st.session_state.callgraph_png and os.path.exists(st.session_state.callgraph_png):
        png_path = st.session_state.callgraph_png

        render_zoomable_image(png_path)

        # Download button
        with open(png_path, 'rb') as f:
            st.download_button(
                label="Download Call Graph",
                data=f,
                file_name="callgraph.png",
                mime="image/png"
            )

        # Raw data expander
        txt_path = os.path.join(project_root, "graph.text")
        if os.path.exists(txt_path):
            with st.expander("View raw call graph data"):
                with open(txt_path, 'r') as f:
                    st.code(f.read(), language='text')

    # Info box
    with st.expander("About Call Graph Analysis"):
        st.markdown("""
        **What is a Call Graph?**
        A directed graph showing which functions call which other functions.

        **How it's generated:**
        - Uses LLVM/Clang static analysis (not regex)
        - Compiles the file to LLVM IR, then analyzes call instructions
        - Function names are demangled using `c++filt`

        **What to look for:**
        - Functions with many outgoing calls (high fan-out) = complex, risky
        - Functions called by many others (high fan-in) = critical, high impact if buggy

        **Reference:** Grove et al. (1997) "Call Graph Construction in Object-Oriented Languages"
        """)