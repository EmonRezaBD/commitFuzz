def parse_graph_text(txt_path):
    """
    Parse LLVM graph.text into a dict.
    Format: [caller]:[callee1],[callee2]
    Returns: {caller: [callee1, callee2]}
    """
    call_map = {}
    with open(txt_path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            match = re.match(r'\[(.+?)\]:\[?(.+)\]?', line)
            if not match:
                continue
            caller      = match.group(1).strip()
            callees_raw = match.group(2).strip()
            callees     = re.findall(r'\[?([^\[\],]+)\]?', callees_raw)
            callees     = [c.strip() for c in callees if c.strip()]
            call_map[caller] = callees
    return call_map


def demangle_name(name):
    """Demangle a single C++ mangled name."""
    if not name.startswith('_Z'):
        return name
    result = subprocess.run(
        ['c++filt', name], capture_output=True, text=True
    )
    clean = result.stdout.strip()
    clean = clean.split('(')[0]
    clean = clean.split('::')[-1]
    return clean


def demangle_map(call_map):
    """Demangle all function names in call_map."""
    clean_map = {}
    for caller, callees in call_map.items():
        clean_caller  = demangle_name(caller)
        clean_callees = [demangle_name(c) for c in callees]
        clean_map[clean_caller] = clean_callees
    return clean_map


def get_available_functions(call_map):
    """Return sorted list of all function names."""
    all_funcs = set(call_map.keys())
    for callees in call_map.values():
        all_funcs.update(callees)
    return sorted(all_funcs)


def build_function_subgraph(call_map, target_func):
    """Build subgraph centered on target_func."""
    G     = nx.DiGraph()
    roles = {}

    G.add_node(target_func)
    roles[target_func] = 'target'

    # Outgoing: target calls these
    if target_func in call_map:
        for callee in call_map[target_func]:
            G.add_node(callee)
            G.add_edge(target_func, callee)
            roles[callee] = 'callee'

    # Incoming: these call target
    for caller, callees in call_map.items():
        if caller == target_func:
            continue
        if target_func in callees:
            G.add_node(caller)
            G.add_edge(caller, target_func)
            roles[caller] = 'caller'

    return G, roles


def render_function_subgraph(G, roles, target_func, output_path):
    """Draw and save function-specific subgraph."""
    if G.number_of_nodes() == 0:
        return None

    color_map = {
        'target': '#ff6600',
        'caller': '#4488ff',
        'callee': '#44bb44',
    }
    node_colors = [color_map.get(roles.get(n, 'callee')) for n in G.nodes()]

    try:
        pos = nx.nx_agraph.graphviz_layout(G, prog='dot')
    except Exception:
        pos = nx.spring_layout(G, seed=42, k=3.0)

    fig, ax = plt.subplots(figsize=(16, 9))
    ax.set_aspect('auto')

    # Auto-fit axis limits first
    if pos:
        xs = [v[0] for v in pos.values()]
        ys = [v[1] for v in pos.values()]
        x_pad = (max(xs) - min(xs)) * 0.3 + 100
        y_pad = (max(ys) - min(ys)) * 0.3 + 80
        ax.set_xlim(min(xs) - x_pad, max(xs) + x_pad)
        ax.set_ylim(min(ys) - y_pad, max(ys) + y_pad)

    # Draw edges first
    nx.draw_networkx_edges(G, pos, ax=ax, arrows=True,
                           arrowsize=20, edge_color='#555555',
                           connectionstyle='arc3,rad=0.05',
                           min_source_margin=45, min_target_margin=45)

    # Draw elliptical nodes using ax.transData + FancyBboxPatch
    renderer = fig.canvas.get_renderer()
    for node in G.nodes():
        x, y  = pos[node]
        role  = roles.get(node, 'callee')
        color = color_map.get(role, '#44bb44')
        label = node

        # Measure text width to size ellipse dynamically
        txt = ax.text(x, y, label, ha='center', va='center',
                      fontsize=9, fontweight='bold', color='white',
                      zorder=5)

        # Get text bounding box in display coords, convert to data coords
        fig.canvas.draw()
        bbox = txt.get_window_extent(renderer=renderer)
        inv  = ax.transData.inverted()
        bbox_data = inv.transform([[bbox.x0, bbox.y0],
                                   [bbox.x1, bbox.y1]])
        w = (bbox_data[1][0] - bbox_data[0][0]) * 1.8
        h = (bbox_data[1][1] - bbox_data[0][1]) * 2.8

        ellipse = mpatches.Ellipse(
            (x, y), width=max(w, 60), height=max(h, 25),
            facecolor=color, edgecolor='white',
            linewidth=1.5, alpha=0.95, zorder=4
        )
        ax.add_patch(ellipse)
        txt.set_zorder(5)

    ax.set_title(f"Call Graph: {target_func}()",
                 fontsize=14, fontweight='bold', pad=15)
    ax.axis('off')

    legend_items = [
        mpatches.Patch(color='#4488ff', label='Callers (call this function)'),
        mpatches.Patch(color='#ff6600', label=f'{target_func} (selected)'),
        mpatches.Patch(color='#44bb44', label='Callees (called by this function)'),
    ]
    ax.legend(handles=legend_items, loc='upper right',
              fontsize=10, framealpha=0.9)

    plt.tight_layout()
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close()
    return output_path


