def demangle_dot(dot_content):
    """Demangle C++ mangled names in dot file safely."""
    def replace_mangled(match):
        name = match.group(0).strip('"')
        result = subprocess.run(
            ['c++filt', name], capture_output=True, text=True
        )
        clean = result.stdout.strip()
        clean = clean.split('(')[0]
        clean = clean.split('::')[-1]
        return f'"{clean}"'
    return re.sub(r'"?_Z\w+"?', replace_mangled, dot_content)


def run_llvm_callgraph(cpp_file_path, project_root, results_dir):
    """Run LLVM CallGraph tool. Returns (png_path, error)."""
    callgraph_script = os.path.join(project_root, "CallGraph", "run.sh")
    try:
        result = subprocess.run(
            ["bash", callgraph_script, cpp_file_path],
            capture_output=True, text=True, cwd=project_root
        )
        if result.returncode != 0:
            return None, f"LLVM Error:\n{result.stderr}"

        dot_path = os.path.join(project_root, "graph.dot")
        if not os.path.exists(dot_path):
            return None, "graph.dot not generated by LLVM"

        clean_dot_path = os.path.join(results_dir, "graph_clean.dot")
        with open(dot_path, 'r') as f:
            dot_content = f.read()

        clean_content = demangle_dot(dot_content)
        with open(clean_dot_path, 'w') as f:
            f.write(clean_content)

        png_path = os.path.join(results_dir, "callgraph.png")
        render_result = subprocess.run(
            ["dot", "-Tpng", clean_dot_path, "-o", png_path],
            capture_output=True, text=True
        )
        if render_result.returncode != 0:
            return None, f"Graphviz Error:\n{render_result.stderr}"

        return png_path, None

    except FileNotFoundError as e:
        return None, f"Tool not found: {e}"
    except Exception as e:
        return None, f"Unexpected error: {e}"


